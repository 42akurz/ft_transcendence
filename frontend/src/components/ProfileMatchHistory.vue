<template>
	<div class="profile-match-history">
		<h2>Match History</h2>
		<div class="score" v-for="match in limitedMatchHistory" :key="match">
			<BaseCardMatchHistory
				:gameInfo="match"
				:currentUserId="user.id"
			/>
		</div>
	</div>
</template>

<script>
	import store from '@/store/index.js'
	import BaseCardMatchHistory from '@/components/BaseCardMatchHistory.vue'
	import axios from 'axios'

	export default {
		name: 'ProfileMatchHistory',

		components: {
			BaseCardMatchHistory
		},

		data() {
			return {
				limitedMatchHistory: []
			}
		},

		props: {
			user: {
				type: Object,
				required: true
			}
		},

		async mounted() {
			this.limitedMatchHistory = await axios.get(`${process.env.VUE_APP_HOST_URL}:3000/score/matchHistory/${this.user.id}/${5}`, {withCredentials: true})
			.then(async (response) => {
				return await response.data;
			})
			.catch((error) => {
				console.log('Error: ' + error.response.data.message)
			})
			console.log(this.limitedMatchHistory)
		},
	}
</script>

<style scoped>
	h2 {
		color: var(--blue-dark);
		letter-spacing: 2px;
		margin: 0 0 30px 0;
	}
</style>